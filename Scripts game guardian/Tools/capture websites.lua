local function searchAndCaptureShader(searchPattern, outputFile)
    gg.clearResults()
   
    gg.setRanges(gg.REGION_ANONYMOUS | gg.REGION_C_ALLOC | gg.REGION_JAVA_HEAP)
    
    gg.searchNumber(searchPattern, gg.TYPE_BYTE, false, gg.SIGN_EQUAL, 0, -1)
    local results = gg.getResults(1)
    if #results == 0 then
    
    os.exit()
    
        return
    end
    local address = results[1].address
    local shaderLines = {}
    local i = address
    local slashCount = 0
    while true do
        local result = gg.getValues({{address = i, flags = gg.TYPE_BYTE}})
        if result and #result > 0 then
            local value = result[1].value
            table.insert(shaderLines, string.char(value))
            if value == 47 then   
                slashCount = slashCount + 1
                if slashCount == 3 then
                    break
                end
            end
        else
            break
        end
        i = i + 1
    end
    local shaderCode = table.concat(shaderLines)

    gg.getResults(1)
    gg.editAll("5", gg.TYPE_BYTE)

    local outputFile = io.open(outputFile, "a")  
    if outputFile then
        outputFile:write(shaderCode .. "\n")  
        outputFile:close()
 
    else
        print("Error writing websites")
    end
end

